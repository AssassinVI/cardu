                                  <p><b>可以綁定多少張 Apple Pay 卡</b></p>
                                  <p>同一部設備最多可綁定 8張卡片，但一張卡片只可與一部 iOS 設備綁定，與當前 iOS 配對的 Apple Watch 也可以再次綁定相同的卡片。</p>

