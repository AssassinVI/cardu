                          <p>
                            香港這個冬天不一樣！香港旅遊局提出「玩轉日夜香港」冬日旅行提案，顛覆台灣旅客對香港的既定旅行印象。旅客白天可以透過與《國家地理》合作的遠足指南擁抱香港的郊野之美，夜間則是欣賞首屆「閃躍維港燈影節」：有來自世界各地及香港藝術家聯合展出共18項精彩燈影藝術作品、在多幢大廈頂樓綻放煙火的特別版「幻彩詠香江」，還有以裝置藝術呈現的創意「魔方樹」及皇后像廣場傳統聖誕樹，再加上各商場璀璨的節慶聖誕燈飾，今年冬日玩轉日夜香港不同風情，感受香港的絕美！
                          </p>

                          <div class="con_img">
                            <img src="../img/component/photo1.jpg" alt="" title="新聞">
                            <p>▲JCB砸重本舉辦「夏日遊樂祭」，在9月底前釋出日本實體商店刷卡2%起現金回饋的好康 (圖/卡優新聞網)</p>
                          </div>

                          <p>
                            　許多人可能不知道，香港這個國際大都會其實約有四分之三的土地屬於郊野，今年港旅局與《國家地理》合作，邀請二位世界頂級攝影師拍攝一系列富有感染力的照片及影片，吸引旅客在十一月至三月的遠足旺季親身投入大自然，探索香港大自然的美景。《香港登山遠足、單車旅遊指南》共規劃觀鳥、賞蝶、賞花、歷史文化、美景拍攝、地質奇觀及海濱單車遊等七大主題路線，整理最值得探訪的生態景觀、奇岩怪石與適合拍照打卡的翠綠山巒等景觀，且多條路線僅距離香港都會區一小時車程就可以抵達，是十分值得喜歡秘境的台灣旅客深入探索的精彩路線。
                          </p>

                           <div class="con_img">
                            <img src="../img/component/photo1.jpg" alt="" title="新聞">
                            <p>▲JCB砸重本舉辦「夏日遊樂祭」，在9月底前釋出日本實體商店刷卡2%起現金回饋的好康 (圖/卡優新聞網)</p>
                          </div>

                          <p>
                            　香港今年於2018年11月29日至2019年2月24日期間舉行首屆「閃躍維港燈影節」，中西區海濱長廊的「國際燈光藝術展」及冬日限定版「幻彩詠香江」將為遊客帶來全新夜間體驗。旅客除了在富創意的展品前拍照打卡，還可以參與其中。其中匈牙利藝術家Viktor Vicsek創作兩個以LED燈泡製成的頭顱《Talking Heads》，呈現不同的面部表情和色彩，同時對觀眾的動作作出反應；荷蘭創作團隊VENIVIDMULTIPLEX的互動單車燈光作品《Lightbattle X》，將邀請觀眾透過踩動單車踏板進行一場光影之戰；至於由香港創作團隊築夢ž社設計及首度展出的《蝠鼠吊金錢》，以香港押業招牌為靈感，帶出香港街道流光溢彩的一面，讓觀眾充分感受香港特色。
                          </p>
                           <div class="con_img">
                            <img src="../img/component/photo1.jpg" alt="" title="新聞">
                            <p>▲JCB砸重本舉辦「夏日遊樂祭」，在9月底前釋出日本實體商店刷卡2%起現金回饋的好康 (圖/卡優新聞網)</p>
                          </div>

                          <p>
                            　被譽為世界最精采的燈光音樂匯演之一的「幻彩詠香江」，今年於特定日子會安排在多幢大廈樓頂綻放煙火，照亮維港兩岸的冬日夜空。至於聖誕節絕不能缺少的聖誕樹，今年位於香港摩天輪旁的《魔方樹》是法國創作團隊1024 Architecture對傳統聖誕樹的全新演繹。高25米的《魔方樹》以發光的鋼架結構作為樹枝，配以巧妙擺放的燈光裝飾以及音樂，構成一場精彩的聖誕匯演。至於中環皇后像廣場的標誌性傳統聖誕樹，將繼續成為旅客熱切期待的景點，並與《魔方樹》共同營造濃厚的節日氣氛。
                          </p>
                           <div class="con_img">
                            <img src="../img/component/photo1.jpg" alt="" title="新聞">
                            <p>▲JCB砸重本舉辦「夏日遊樂祭」，在9月底前釋出日本實體商店刷卡2%起現金回饋的好康 (圖/卡優新聞網)</p>
                          </div>

                          <p>
                            　除了「閃躍維港燈影節」，維港兩岸的摩天大樓、大街小巷也會掛滿五光十色的聖誕燈飾，華麗亮眼。各大商場更會爭奇鬥豔佈置最吸引旅客目光的聖誕主題，還有最不能錯過的香港倒數煙火音樂匯演，也會於12月31日午夜在維港維多利亞港上空舉行。一連串節慶及精采活動，讓旅客來港歡度冬日假期更加難忘，更加賞心悅目！
                          </p>
