                        <h2 class="h2">JR東日本鐵路周遊券(東北地區)</h2>
                        <br>
                        <p2 class="p2">JR東日本周遊券(東北地區)是專門為持有非日本護照的外籍旅客(日本逗留天數為90天以内之短期逗留者)準備的特惠周遊券。只要有此周遊券即可不限次數乘坐JR東日本鐵路周遊券(東北地區)免費區域內的JR東日本線。<br>
除了從成田機場或羽田機場到東京圈的機場專線和東京圈內的所有線路以外，還可無限次免費搭乘開往青森、仙台的新幹線和特快列車。除JR東日本路線以外，還包括開往伊豆、日光等地區的線路。
