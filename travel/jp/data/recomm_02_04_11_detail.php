                            <div style="width:250px; display:inline-block; vertical-align:top"><img src="data/images/<?= $pk ?>_11-SUICA.jpg" width="250" alt=""/></div>
                            <div style="width:300px; padding-left:30px; display:inline-block; text-align: left;"><p2 class="p2">Suica（スイカ）是英文Super Urban Intelligent Card（超級都會智慧卡）的英文縮寫。因為讀音和日文中的「西瓜」一樣，因此也俗稱為「西瓜卡」。和台灣的「悠遊卡」用法相同，加值後就可憑一張卡東京走透透，也可以用來購物，若不想和售票機苦戰的話(真心覺得日本售票機好複雜呀)，一張西瓜卡會節省你不少時間！
