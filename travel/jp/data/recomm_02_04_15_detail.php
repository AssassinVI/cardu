                        <h3 class="h3" style="padding-top:20px;">2.SUNQ PASS</h3>
                        <p2 class="p2">SUNQ PASS是可自由搭乘三天九州島内的長途巴士及普通路線巴士的免資車票。可以利用的巴士路線數約達2,400個，含括約99%的九州島内巴士。<br>
SUNQ PASS有「全九州4日券」、「全九州3日券」和「北部九州3日券」三種。<br>
1.「全九州」可在九州內七縣＋下關使用，3日券10,000日圓，4日券14,000日圓。<br>
2.「北部九州」可在下關、福岡、佐賀、長崎、大分、熊本三日內自由使用，8000日圓。</p2>
