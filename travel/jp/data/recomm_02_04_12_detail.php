                        <p2 class="p2">「HARUKA 關空特急」（又稱遙號或遙遠號，以下簡稱 HARUKA）是 JR 西日本推出的機場高速特快車，該列車由關西機場出發，沿途只停靠天王寺站、新大阪站及京都站，適合從關西機場往返大阪及京都的旅客搭乘</p2>
                        <h2 class="h2" style="padding-top:30px;">HARUKA 票價與新版 ICOCA＆HARUKA 優惠套票</h2>
                        <div id="page_pic" style="padding-top:20px;">
                            <a href="http://www.westjr.co.jp/global/tc/ticket/icoca-haruka" target="_blank"><img src="data/images/<?= $pk ?>_05-pic.jpg" width="600" alt=""/></a>
                        </div>
                        <p2 class="p2">直接購買 HARUKA 車票十分昂貴。不過，JR 西日本針對外國旅客推出了較划算的「ICOCA＆HARUKA 優惠套票」，並於 2016 年 3 月 1 日開始銷售新版本。
新版的套票內容同樣包含「ICOCA 一張」（ICOCA 就是 JR 西日本推出的悠遊卡）及「HARUKA 單程或來回折扣券」。套票中的 ICOCA 一樣是 2000 日圓，內含 1500 日圓儲值金額及 500 日圓押金（退卡時會退還）。HARUKA 折扣券則改成依距離計費，越遠就越貴，可免費轉乘的 JR 路線也隨目的地不同而異。不過最大的改變，就是已經有 ICOCA 的旅客，只要出示卡片及護照便可單獨購買套票中的 HARUKA 折扣券。但沒有 ICOCA 的旅客，還是只能持護照購買整組套票
</p2>
